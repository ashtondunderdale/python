def add(numberOne, numberTwo):
    """adds a number"""

    print(numberOne, "+", numberTwo, "=", numberOne + numberTwo)

def subtract(numberOne, numberTwo):
    """subtracts a number"""

    print(numberOne, "-", numberTwo, "=", numberOne - numberTwo)

def multiply(numberOne, numberTwo):
    """multiplies a number"""

    print(numberOne, "*", numberTwo, "=", numberOne * numberTwo)

def divide(numberOne, numberTwo):
    """divides a number"""

    print(numberOne, "/", numberTwo, "=", numberOne / numberTwo)

